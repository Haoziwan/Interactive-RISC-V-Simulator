# S型指令测试程序
# 测试所有S型指令的基本功能和边界条件

# 初始化测试数据
addi x1, x0, 1234     # x1 = 1234 (0x4D2)
addi x2, x0, -56      # x2 = -56
addi x3, x0, 0x7FF    # x3 = 2047
addi x4, x0, 0x123    # x4 = 291

# 测试字存储指令
sw x1, 0(x0)         # mem[0] = 1234
sw x2, 4(x0)         # mem[4] = -56
sw x3, 8(x0)         # mem[8] = 2047
sw x4, 12(x0)        # mem[12] = 0x123

# 测试半字存储指令
sh x1, 16(x0)        # mem[16] = 1234 (低16位)
sh x2, 18(x0)        # mem[18] = -56 (低16位)
sh x3, 20(x0)        # mem[20] = 2047 (低16位)
sh x4, 22(x0)        # mem[22] = 0x123 (低16位)

# 测试字节存储指令
sb x1, 24(x0)        # mem[24] = 210 (0x4D2的低8位)
sb x2, 25(x0)        # mem[25] = -56 (低8位)
sb x3, 26(x0)        # mem[26] = 255 (0x7FF的低8位)
sb x4, 27(x0)        # mem[27] = 0x23

# 测试不同基址的存储
addi x5, x0, 100     # x5 = 100 (基址)
sw x1, 0(x5)         # mem[100] = 1234
sh x2, 4(x5)         # mem[104] = -56 (半字)
sb x3, 8(x5)         # mem[108] = 255 (字节)

# 测试边界偏移量
sw x1, -2048(x5)     # 测试最小偏移量
sw x2, 2047(x5)      # 测试最大偏移量

# 测试连续存储
addi x6, x0, 1       # x6 = 1
addi x7, x0, 2       # x7 = 2
addi x8, x0, 3       # x8 = 3
addi x9, x0, 4       # x9 = 4
sw x6, 0(x0)         # 连续存储4个字
sw x7, 4(x0)
sw x8, 8(x0)
sw x9, 12(x0)

# 完成测试
add x0, x0, x0       # NOP指令，标记测试结束